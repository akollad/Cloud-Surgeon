/**
 * Cloud-Surgeon Logo Component
 *
 * Variants
 * ────────
 * "full"       — complete SVG mark (scalpel + clouds + SURGEON wordmark)
 * "mark"       — icon only (upper scalpel + cloud, no wordmark)
 * "horizontal" — SVG mark + "Cloud-Surgeon" text side by side
 *
 * Themes (controls fill color)
 * ────────────────────────────
 * "brand"  — #124896 — use on white / light surfaces
 * "white"  — #ffffff — use on dark / navy surfaces
 * "ink"    — #0a1628 — use on mid-tone gray / neutral surfaces
 * "auto"   — inherits CSS currentColor from parent
 *
 * Sizes map to height in px. Width is proportional.
 */

import { cn } from "@/lib/utils";

export type LogoTheme   = "brand" | "white" | "ink" | "auto";
export type LogoVariant = "full" | "mark" | "horizontal";
export type LogoSize    = "xs" | "sm" | "md" | "lg" | "xl" | "2xl";

interface LogoProps {
  variant?: LogoVariant;
  theme?: LogoTheme;
  size?: LogoSize;
  className?: string;
  "aria-label"?: string;
}

const THEME_COLORS: Record<LogoTheme, string> = {
  brand: "#124896",
  white: "#ffffff",
  ink:   "#0a1628",
  auto:  "currentColor",
};

const SIZE_H: Record<LogoSize, number> = {
  xs:  16,
  sm:  24,
  md:  32,
  lg:  48,
  xl:  72,
  "2xl": 96,
};

// The SVG viewBox (from the original Illustrator export)
const FULL_VB    = "0 0 150.53 140.33";
const MARK_VB    = "0 0 151 97";     // crop to the upper scalpel+circle mark only (full width needed)
const WORDMARK_VB = "5 126 142 15";  // crop to the SURGEON text only

export function Logo({
  variant = "full",
  theme   = "brand",
  size    = "md",
  className,
  "aria-label": ariaLabel = "Cloud-Surgeon",
}: LogoProps) {
  const color   = THEME_COLORS[theme];
  const height  = SIZE_H[size];

  // Mark: viewBox crops to icon, preserve aspect ratio of that crop
  // full viewBox 150.53 × 140.33 → mark crop 151 × 97 → w/h ≈ 1.557
  // full crop   150.53 × 140.33 → w/h ≈ 1.073
  const markAR  = 151 / 97;
  const fullAR  = 150.53 / 140.33;

  if (variant === "horizontal") {
    const iconH = height;
    const iconW = Math.round(iconH * markAR);
    return (
      <div
        className={cn("flex items-center select-none", className)}
        aria-label={ariaLabel}
        role="img"
      >
        {/* Icon mark */}
        <svg
          viewBox={MARK_VB}
          width={iconW}
          height={iconH}
          xmlns="http://www.w3.org/2000/svg"
          aria-hidden="true"
          style={{ flexShrink: 0 }}
        >
          <LogoPaths color={color} />
        </svg>
        {/* Text lockup */}
        <div
          className="flex flex-col leading-none select-none"
          style={{ marginLeft: Math.round(iconH * 0.4) }}
        >
          <span
            style={{
              fontFamily: "'Geist', sans-serif",
              fontWeight: 600,
              fontSize: `${Math.round(iconH * 0.45)}px`,
              letterSpacing: "-0.02em",
              color,
              lineHeight: 1.1,
            }}
          >
            Cloud
          </span>
          <span
            style={{
              fontFamily: "'Geist', sans-serif",
              fontWeight: 700,
              fontSize: `${Math.round(iconH * 0.45)}px`,
              letterSpacing: "-0.02em",
              color,
              lineHeight: 1.1,
            }}
          >
            Surgeon
          </span>
        </div>
      </div>
    );
  }

  const vb = variant === "mark" ? MARK_VB : FULL_VB;
  const ar = variant === "mark" ? markAR  : fullAR;
  const w  = Math.round(height * ar);

  return (
    <svg
      viewBox={vb}
      width={w}
      height={height}
      xmlns="http://www.w3.org/2000/svg"
      role="img"
      aria-label={ariaLabel}
      className={cn("select-none", className)}
    >
      <LogoPaths color={color} />
    </svg>
  );
}

